    private void fireOyenteHeaderCRUD(boolean n, boolean e, boolean l) {
        for (final HeaderCRUDListener entry : this.oyentes) {
            if (entry != null) {
                if (n) {
                    entry.nuevo();
                }
                if (e) {
                    entry.editar();
                }
                if (l) {
                    entry.listar();
                }
            }
        }
    }
    
